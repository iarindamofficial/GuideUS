package guideus.arindam.com.guideus;

public class UserProfile {

    public String userGender;
    public String userEmailAdmission;
    public String userNameAdmission;
    public String userFatherName;
    public String userPhoneNumberAdmission;
    public String userAddress;
    public String userStreamName;


    public UserProfile(String userGender, String userEmailAdmission, String userNameAdmission, String userFatherName, String userPhoneNumberAdmission, String userAddress, String userStreamName) {
        this.userGender = userGender;
        this.userEmailAdmission = userEmailAdmission;
        this.userNameAdmission = userNameAdmission;
        this.userFatherName = userFatherName;
        this.userPhoneNumberAdmission = userPhoneNumberAdmission;
        this.userAddress = userAddress;
        this.userStreamName = userStreamName;
    }
}
